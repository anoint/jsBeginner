//비주얼 스튜디오 코드에서 자동 문법 완성 기능으로 switch를 암기불필요
var subject = "자바스크립트";
switch (subject) {
    case 'C언어':
        console.log('초보자를 위한 C++ 200제')
        break;
    case '자바스크립트':
            console.log('초보자를 위한 자바스크립트 200제')
            break;
    default:
         console.log('이젠 초보자가 아닙니다.');
        break;
}